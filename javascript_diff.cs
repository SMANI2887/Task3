//3.Read about the difference between window, screen and document in javascript

1.window.screen or just screen is a small information object about physical screen dimensions.

2.window.document or just document is the main object of the potentially visible (or better yet: rendered) document object model/DOM.

3.Since window is the global object, you can reference any properties of it with just the property name - so you do not have to write down window. - it will be figured out by the runtime.